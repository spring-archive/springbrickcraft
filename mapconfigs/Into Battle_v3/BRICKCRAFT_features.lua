end

function Bush  (x,z)
	newBush = {}
	newBush.x = x+math.random (-100,100)
	newBush.z = z+math.random (-100,100)
	newBush.name = "f_tptribush"
	table.insert (features, newBush)
end

function Tree  (x,z)
	newBush = {}
	newBush.x = x
	newBush.z = z	
	newBush.name = "f_tptree"
	table.insert (features, newBush)
	Bush  (x+math.random(-60,60),z+math.random(-60,60))
	Bush  (x+math.random(-60,60),z+math.random(-60,60))
end

