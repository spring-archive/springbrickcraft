function gadget:Initialize()
	local level = loadLevel()
	local offx=100
	local offz=100
	local a = 100
	for zi = 1, #level, 1 do
		for xi = 1, #level[zi], 1 do
			local x=offx+  (xi*a)
			local z=offz+ (zi*a)
			if (level[zi][xi]==1) then 
				Spring.CreateUnit("tpsmallfac", x, 50, z, 0, 0)
			end
			--if (level[zi][xi]==0) then 
